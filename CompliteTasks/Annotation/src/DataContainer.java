
public class DataContainer {

    int num;

    String str;


    public DataContainer(int num, String str) {
        this.num = num;
        this.str = str;
    }


    public DataContainer() {
    }

    public void setNum(int num) {
        this.num = num;
    }

    public void setStr(String str) {
        this.str = str;
    }


    public int getNum() {
        return num;
    }

    public String getStr() {
        return str;
    }


}
